package fi.metropolia.java7features;

public class HexLiterals {

	private int hexEight = 0x8;
	private int hexHundred = 0x64;
	private int hexThousand = 0x3E8;

	public int getHexEight() {
		return hexEight;
	}

	public int getHexHundred() {
		return hexHundred;
	}

	public int getHexThousand() {
		return hexThousand;
	}

}
