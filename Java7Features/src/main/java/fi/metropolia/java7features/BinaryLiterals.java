package fi.metropolia.java7features;

public class BinaryLiterals {

	private int binaryEight = 0b1000;
	private int binaryHundred = 0b1100100;
	private int binaryThousand = 0b1111101000;

	public int getBinaryEight() {
		return binaryEight;
	}

	public int getBinaryHundred() {
		return binaryHundred;
	}

	public int getBinaryThousand() {
		return binaryThousand;
	}

}
